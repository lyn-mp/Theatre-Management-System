    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
       DefaultTableModel tb2Model = (DefaultTableModel) jTable1.getModel();
        if(tb2Model.getRowCount()==0){
            JOptionPane.showMessageDialog(this,"Table is empty");
        }
        else
        {
         try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/projectdb","root","lynjsql#22");
            for(int i = 0;i<tb2Model.getRowCount();i++)
            {
                String aid = tb2Model.getValueAt(i,0).toString();
                String aname = tb2Model.getValueAt(i,1).toString();
                String apass = tb2Model.getValueAt(i,2).toString();
                String query  = "insert into admin(admin_id,admin_name,password) values(?,?,?)";
                PreparedStatement ps = con.prepareStatement(query);
                ps.setString(1,aid);
                ps.setString(2,aname);
                ps.setString(3,apass);
                ps.executeUpdate();
            }
             JOptionPane.showMessageDialog(this,"Data inserted successfully ");
             tb2Model.setRowCount(0);
             con.close();
         }catch (Exception ex) {
            Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
        } 
        } 
    }//GEN-LAST:event_saveActionPerformed

