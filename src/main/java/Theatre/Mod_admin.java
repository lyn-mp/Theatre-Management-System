    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
    
        DefaultTableModel tb5Model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRowCount()==1){
            try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/projectdb","root","lynjsql#22");
         String aid = admin_id.getText();
         String aname = admin_name.getText();
         String apass = password.getText();
         tb5Model.setValueAt(aid,jTable1.getSelectedRow(),0);
         tb5Model.setValueAt(aname,jTable1.getSelectedRow(),1);
         tb5Model.setValueAt(apass,jTable1.getSelectedRow(),2);
         String admin = tb5Model.getValueAt(jTable1.getSelectedRow(),0).toString();
         String query = "UPDATE admin SET admin_id = ?, admin_name = ?,password = ? where admin_id = ?";
             PreparedStatement ps = con.prepareStatement(query);
            ps.setString(1,aid);
            ps.setString(2,aname);
            ps.setString(3,apass);
            ps.setString(4,admin);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Updated Successfully");
        }catch (Exception ex) {
            Logger.getLogger(customer.class.getName()).log(Level.SEVERE, null, ex);
        } 
            }
        else if(jTable1.getSelectedRowCount()==0){
        JOptionPane.showMessageDialog(this,"Table is empty");
        }
        else{
            JOptionPane.showMessageDialog(this,"Please select single row for update");
        }
            
    }//GEN-LAST:event_updateActionPerformed

